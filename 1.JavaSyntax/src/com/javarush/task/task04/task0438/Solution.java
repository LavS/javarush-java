package com.javarush.task.task04.task0438;

/* 
Рисуем линии
*/

public class Solution {
    public static void main(String[] args) throws Exception {
        String s = "";
        for (int i = 0; i < 10; i++) {
            s = s + 8;
        }
        System.out.println(s);
        for (int i = 0; i < 10; i++) {
            System.out.println(8);
        }

    }
}
